import { notFound } from "next/navigation";
import { headers } from "next/headers";
import type { Metadata } from "next";
import { getPublicAscent } from "@/lib/services/public-ascent.service";
import { getRarityId, RARITY_COLORS, RARITY_LABELS, RARITY_EP } from "@/lib/rarity";
import { PeakadexLogo } from "@/components/brand/Logo";
import Link from "next/link";
import { getT, isValidLocale } from "@/lib/i18n";

const APP_URL =
  process.env.APP_URL ?? process.env.NEXTAUTH_URL ?? "https://www.peakadex.com";

async function getBaseUrl(): Promise<string> {
  try {
    const h = await headers();
    const host = h.get("host");
    if (host) return host.includes("localhost") ? `http://${host}` : `https://${host}`;
  } catch {}
  return APP_URL;
}

// ── Metadata ──────────────────────────────────────────────────────────────────

export async function generateMetadata({
  params,
  searchParams,
}: {
  params: Promise<{ id: string }>;
  searchParams: Promise<{ lang?: string }>;
}): Promise<Metadata> {
  const { id } = await params;
  const { lang } = await searchParams;
  const locale = isValidLocale(lang ?? "") ? lang! : "es";
  const t = getT(locale);

  const [ascent, baseUrl] = await Promise.all([getPublicAscent(id), getBaseUrl()]);
  if (!ascent) return { title: "Peakadex" };

  const rarity = getRarityId(ascent.peak.altitudeM);
  const rarityLabel = RARITY_LABELS[rarity];

  const title = `${ascent.user.name} ha capturado ${ascent.peak.name}`;
  const description = `Altitud: ${ascent.peak.altitudeM} m · Rareza: ✿ ${rarityLabel}`;

  // Card-style OG image generated by /api/og/[id] (sharp-based, no next/og).
  // Falls back to the raw photo URL if the API is unavailable.
  const ogImage = `${baseUrl}/api/og/${id}`;

  return {
    title,
    description,
    openGraph: {
      title,
      description,
      url: `${baseUrl}/ascent/${id}`,
      siteName: "Peakadex",
      images: [
        {
          url: ogImage,
          width: 1080,
          height: 1350,
          alt: title,
        },
      ],
      type: "article",
    },
    twitter: {
      card: "summary",
      title,
      description,
      images: [ogImage],
    },
  };
}

// ── Helpers ───────────────────────────────────────────────────────────────────

function formatDate(iso: string, locale: string) {
  return new Date(iso).toLocaleDateString(locale, {
    day: "numeric",
    month: "long",
    year: "numeric",
  });
}

function InitialsAvatar({ name, size = 40 }: { name: string; size?: number }) {
  const parts = name.trim().split(" ");
  const initials =
    parts.length >= 2
      ? (parts[0][0] + parts[parts.length - 1][0]).toUpperCase()
      : (name[0]?.toUpperCase() ?? "?");
  return (
    <div
      style={{
        width: size,
        height: size,
        borderRadius: "50%",
        background: "linear-gradient(135deg, #0369a1 0%, #0ea5e9 100%)",
        color: "white",
        fontSize: size * 0.38,
        fontWeight: 700,
        display: "flex",
        alignItems: "center",
        justifyContent: "center",
        flexShrink: 0,
      }}
    >
      {initials}
    </div>
  );
}

